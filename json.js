function formatPrice(price) {
    if (typeof price !== 'number') {
        return "Precio no disponible"; // o un mensaje de error adecuado
    }
    return '$' + price.toFixed(2).replace(/\d(?=(\d{3})+\.)/g, '$&,');
}

